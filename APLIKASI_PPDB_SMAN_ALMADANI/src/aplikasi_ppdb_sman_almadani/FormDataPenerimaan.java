    private void tampilppdb(){
        Object[] baris = {"NISN","Nama Siswa/Siswi","Jenis Kelamin","Nilai Rata-Rata","Nilai Rata-Rata Utk. Lulus","Kelengkapan Berkas","Status Administrasi","Status Kelulusan/Penerimaan"};
        
        tabmode = new DefaultTableModel(null,baris);
        tabmode.setRowCount(0);
        java.sql.Connection KoneksiPPDB = new KoneksiAplikasi().connect();
         try {
            Statement ppdb = KoneksiPPDB.createStatement();
            String query = "SELECT * FROM data_penerimaan";
            ResultSet rs = ppdb.executeQuery(query);
            
            while (rs.next()){
                String nisn = rs.getString("nisn");
                String nama = rs.getString("nama");
                String jk = rs.getString("jk");
                String nilai_rata2 = rs.getString("nilai_rata2");
                String nilai_rata2_lulus = rs.getString("nilai_rata2_lulus");
                String berkas = rs.getString("berkas");
                String sts_adm = rs.getString("sts_adm");
                String sts_lulus = rs.getString("sts_lulus");
                String[] data = {nisn,nama,jk,nilai_rata2,nilai_rata2_lulus,berkas,sts_adm,sts_lulus};
                tabmode.addRow(data);
            }
        } catch (SQLException ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Menampilkan Data Gagal", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
         jTable2.setModel(tabmode);
    }
