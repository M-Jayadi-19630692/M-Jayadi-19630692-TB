    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
       String nisn = jTextField1.getText();
       String nama = jTextField2.getText();
       String jk = jComboBox6.getSelectedItem().toString();
       String nilai_rata2 = jTextField4.getText();
       String nilai_rata2_lulus = jTextField5.getText();    
       String berkas = jComboBox1.getSelectedItem().toString();
       String sts_adm = jComboBox4.getSelectedItem().toString();
       String sts_lulus = jComboBox5.getSelectedItem().toString();
       java.sql.Connection KoneksiPPDB = new KoneksiAplikasi().connect();
       try{
           Statement ppdb = KoneksiPPDB.createStatement();
           String query = "INSERT INTO data_penerimaan(nisn,nama,jk,nilai_rata2,nilai_rata2_lulus,berkas,sts_adm,sts_lulus)"+"VALUES('"+nisn+"', '"+nama+"', '"+jk+"', '"+nilai_rata2+"', '"+nilai_rata2_lulus+"', '"+berkas+"', '"+sts_adm+"',  '"+sts_lulus+"')";
           System.out.print(query);
           int berhasil = ppdb.executeUpdate(query);
           if (berhasil == 1) {
                JOptionPane.showMessageDialog(null, "Data Berhasil Dimasukkan");
                tampilppdb();
                reset();
            }else{
                JOptionPane.showMessageDialog(null, "Data Gagal Dimasukkan");
                reset();
            }
             KoneksiPPDB.close();
        } catch (SQLException ex){
            JOptionPane.showMessageDialog(null, "Terjadi Kesalahan Pada Database");
            reset(); 
        }
    }//GEN-LAST:event_jButton2ActionPerformed

